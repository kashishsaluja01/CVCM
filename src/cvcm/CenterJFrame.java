    private void SearchjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchjButton2ActionPerformed
     if(AddressjTextField1.getText().equals(""))
            ErrorjLabel1.setText("Enter centre's address");
        else
             {
            cobj.Connect();
            try
            {
                pst = cobj.con.prepareStatement("Select * from tbcenter where address=?");
                pst.setString(1,AddressjTextField1.getText());
                    rst = pst.executeQuery();
                    if(rst.next())
                    {
                    ErrorjLabel1.setText("Record Found");
                    CenterID=rst.getLong(1);
                    AddressjTextField1.setText(rst.getString(2).trim());
                    PhonejTextField2.setText(rst.getString(3).trim());
                    EmailjTextField3.setText(rst.getString(4).trim());
                    InchargeNamejTextField4.setText(rst.getString(5).trim());
                    TimingsjTextField5.setText(rst.getString(6).trim());
                    
                    }
else
                    {
                        ErrorjLabel1.setText("Record doesnot exist");
                    }
            }
            catch(Exception ex)
            {
                System.out.print("Error in save"+ex);
            }

cobj.DisConnect();
        }   // TODO add your handling code here:
    }//GEN-LAST:event_SearchjButton2ActionPerformed

