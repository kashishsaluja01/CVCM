    private void UpdatejButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatejButton3ActionPerformed
        // TODO add your handling code here:
        if(AddressjTextField1.getText().equals(""))
            ErrorjLabel1.setText("Enter center's address");
        else
            if(PhonejTextField2.getText().equals(""))
            ErrorjLabel1.setText("Enter centre's phone number");
        else
            if(EmailjLabel3.getText().equals(""))
            ErrorjLabel1.setText("Enter centre's email");
        else
            if(InchargeNamejTextField4.getText().equals(""))
            ErrorjLabel1.setText("Enter incharge name");
        else
            if(TimingsjTextField5.getText().equals(""))
            ErrorjLabel1.setText("Enter centr's timings number");
        
        else
            {
            cobj.Connect();
            try
            {
                pst = cobj.con.prepareCall("{call prcUpdateCenter(?,?,?,?,?,?)}");
                pst.setLong(1, CenterID);
                pst.setString(2,AddressjTextField1.getText());
                pst.setString(3,PhonejTextField2.getText());
                pst.setString(4,EmailjTextField3.getText());
                pst.setString(5,InchargeNamejTextField4.getText());
                pst.setString(6,TimingsjLabel5.getText());
                
                if(pst.execute()==false)
                    if(pst.getUpdateCount()>0)
                    {
                    ErrorjLabel1.setText("Record updated");
                    AddressjTextField1.setText("");
                    PhonejTextField2.setText("");
                    EmailjTextField3.setText("");
                    InchargeNamejTextField4.setText("");
                    TimingsjTextField5.setText("");
                    }

            }
            catch(Exception ex)
            {
                System.out.print("Error in update"+ex);
            }

cobj.DisConnect();
        }
    }//GEN-LAST:event_UpdatejButton3ActionPerformed

